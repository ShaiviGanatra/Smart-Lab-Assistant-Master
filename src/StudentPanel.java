    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if("".equals(txtStudentId.getText())){
            JOptionPane.showMessageDialog(null,"Please select record to delete");
        }
        else{
            int btn = JOptionPane.showConfirmDialog(null,"Are you sure you wanna delete the record?","Delete",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(btn == JOptionPane.YES_OPTION){
                try{
                    String sql = "DELETE FROM student WHERE sid = ?";
                    preparedStatement = conn.prepareStatement(sql);
                    preparedStatement.setInt(1, Integer.parseInt(txtStudentId.getText()));
                    preparedStatement.execute();
                    JOptionPane.showMessageDialog(null,"Record Deleted Successfully!");
                    updatejStudentTableData(StudentPanel.sql);
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(null,"Issue:"+e.getMessage());
                }
                finally{
                    try{
                        preparedStatement.close();
                    }
                    catch(Exception e){}
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "UPDATE student SET name = ?,enrollmentno = ?, rollno = ?, semester = ?,"
            + "branch = ?, batch = ?, studimage = ? WHERE sid = ?";
            preparedStatement = conn.prepareStatement(sql);
            preparedStatement.setString(1, txtName.getText());
            preparedStatement.setString(2, txtEnrollmentNo.getText());
            preparedStatement.setString(3, txtRollNo.getText());
            preparedStatement.setString(4, txtSemester.getText());
            preparedStatement.setString(5, txtBranch.getText());
            preparedStatement.setString(6, txtBatch.getText());
            preparedStatement.setBytes(7, byteImage);
            preparedStatement.setInt(8, Integer.parseInt(txtStudentId.getText()));
            preparedStatement.execute();
            JOptionPane.showMessageDialog(null,"Record Updated Successfully");
            updatejStudentTableData(StudentPanel.sql);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Issue:"+e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

