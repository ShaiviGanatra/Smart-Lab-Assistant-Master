    private void btnUploadImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadImageActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.showOpenDialog(null);
        File sourceFile = fileChooser.getSelectedFile();
        if(sourceFile!=null){
            imagePath = sourceFile.getAbsolutePath();
            //txtImagePath.setText(imagePath);
            try{
                File image = new File(imagePath);
                BufferedImage thumbnail = Thumbnails.of(image).size(180,220).asBufferedImage();

                /******************************************************************************
                * ****************************************************************************
                * The Below Code is used to convert the image which is converted to thumbnail
                * to byte array so that we can store the byte array in our database as blob
                * data type
                * ****************************************************************************
                * ****************************************************************************
                */
                ByteArrayOutputStream os = new ByteArrayOutputStream();
                ImageIO.write(thumbnail,"jpg", os);
                byteImage = os.toByteArray();
                /******************************************************************************
                * ****************************************************************************
                */

                viewImage = new ImageIcon(thumbnail);
                lblImage.setIcon(viewImage);
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,"Issue:"+e.getMessage());
            }
        }
    }//GEN-LAST:event_btnUploadImageActionPerformed

    private void jStudentTableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jStudentTableDataMouseClicked
        // TODO add your handling code here:
        int selectedRow = jStudentTableData.getSelectedRow();
        String selectedSId = jStudentTableData.getModel().getValueAt(selectedRow, 0).toString();
        try{
            String sql = "SELECT * FROM student WHERE sid = " + selectedSId;
            preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            if(rs.next()){
                txtStudentId.setText(rs.getString("sid"));
                txtName.setText(rs.getString("name"));
                txtEnrollmentNo.setText(rs.getString("enrollmentno"));
                txtRollNo.setText(rs.getString("rollno"));
                txtSemester.setText(rs.getString("semester"));
                txtBranch.setText(rs.getString("branch"));
                txtBatch.setText(rs.getString("batch"));
                byte [] selectedImage = rs.getBytes("studimage");
                byteImage = selectedImage;
                BufferedImage img = Thumbnails.of(ImageIO.read(new ByteArrayInputStream(selectedImage))).size(180,220).asBufferedImage();
                ImageIcon i = new ImageIcon(img);
                lblImage.setIcon(i);
            }
        }
        catch(Exception e){
            
        }
    }//GEN-LAST:event_jStudentTableDataMouseClicked

