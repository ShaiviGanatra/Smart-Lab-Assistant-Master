    private void updatejSubjectDatesTableData(){
        try{
            String sql = "SELECT experimentno, DOP, DOS FROM experiment WHERE subid ="+txtSubId.getText();
            preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            jSubjectDatesTableData.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Issue:"+e.getMessage());
        }
        finally{
            try{
            rs.close();
            preparedStatement.close();
            }
            catch(Exception e){
            JOptionPane.showMessageDialog(null,"Issue:"+e.getMessage());    
            }
        }
    }
    
    
    
