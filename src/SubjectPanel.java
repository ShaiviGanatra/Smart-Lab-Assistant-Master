    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "INSERT INTO subject(subjectname, semester,branch,batch) VALUES(?,?,?,?)";
            preparedStatement = conn.prepareStatement(sql);
            preparedStatement.setString(1, txtSubjectName.getText());
            preparedStatement.setInt(2, Integer.parseInt(txtSemester.getText()));
            preparedStatement.setString(3, txtBranch.getText());
            preparedStatement.setString(4, txtBatch.getText());
            preparedStatement.execute();
            JOptionPane.showMessageDialog(null,"Record Inserted Successfully");
            updatejSubjectTableData();
        }
        catch(Exception e){}
    }//GEN-LAST:event_btnSaveActionPerformed

    private void jSubjectTableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jSubjectTableDataMouseClicked
        // TODO add your handling code here:
        int selectedRow = jSubjectTableData.getSelectedRow();
        String selectedSubId = jSubjectTableData.getModel().getValueAt(selectedRow, 0).toString();
        try{
            String sql = "SELECT * FROM subject WHERE subid = " + selectedSubId;
            preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            if(rs.next()){
                txtSubId.setText(rs.getString("subid"));
                txtSubjectName.setText(rs.getString("subjectname"));
                txtSemester.setText(rs.getString("semester"));
                txtBranch.setText(rs.getString("branch"));
                txtBatch.setText(rs.getString("batch"));
            }
            updatejSubjectDatesTableData();
        }
        catch(Exception e){
            
        }
    }//GEN-LAST:event_jSubjectTableDataMouseClicked

