    private void updatejReportTableData(String batch){
        String subjectname = "'"+txtSubject.getText()+"'";
        String sql = "SELECT student.name, student.rollno, ((c+p+a)) as avg_marks FROM studentmarks, student WHERE studentmarks.studentname = student.name and studentmarks.subjectname ="+subjectname+" and student.batch="+batch+"GROUP BY student.rollno";

        try{
            preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            jReportTableData.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Issue:"+e.getMessage());
        }
        finally{
            try{
            rs.close();
            preparedStatement.close();
            }
            catch(Exception e){
            JOptionPane.showMessageDialog(null,"Issue:"+e.getMessage());    
            }
        }
    }

