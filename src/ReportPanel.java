    private void updatejSubjectTableData(){
        try{
            String sql = "SELECT * FROM subject ORDER BY batch ASC";
            preparedStatement = conn.prepareStatement(sql);
            rs = preparedStatement.executeQuery();
            jSubjectTableData.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Issue:"+e.getMessage());
        }
        finally{
            try{
            rs.close();
            preparedStatement.close();
            }
            catch(Exception e){
            JOptionPane.showMessageDialog(null,"Issue:"+e.getMessage());    
            }
        }
    }
